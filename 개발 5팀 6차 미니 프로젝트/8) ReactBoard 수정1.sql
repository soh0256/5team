  CREATE TABLE "REACT"."MEMBER" 
   (	"PK" NUMBER(11,0), 
	"ID" VARCHAR2(100 BYTE), 
	"PASSWORD" VARCHAR2(100 BYTE), 
	"NAME" VARCHAR2(100 BYTE),
	"MAIL" VARCHAR2(30 BYTE),
	"PHONE" VARCHAR2(20 BYTE),
	"PROFILE" VARCHAR2(100 BYTE),
	"FULLADDRESS" VARCHAR2(100 BYTE),
	"SUBADDRESS" VARCHAR2(100 BYTE)
   );

